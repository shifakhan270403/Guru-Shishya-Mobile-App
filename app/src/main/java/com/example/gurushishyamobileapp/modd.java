package com.example.gurushishyamobileapp;

public class modd {
    String f,u,p,pw,pr;

    public modd(String f, String u, String p, String pw, String pr) {
        this.f = f;
        this.u = u;
        this.p = p;
        this.pw = pw;
        this.pr = pr;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getPw() {
        return pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }

    public String getPr() {
        return pr;
    }

    public void setPr(String pr) {
        this.pr = pr;
    }
}
